package br.com.jv.estruturasequencial;

public class ExpressaoAritmeticas {

	/*Exemplos de expressões aritméticas*/
	
	public static void main(String[] args) {
		
		int n1 = 2*6/3;
		int n2 = 3+2*4;
		int n3 = (3+2)*4;
		int n4 = 60/(3+2)*4;
		int n5 = 60/((3+2)*4);
		
		System.out.println("Resultado N1: " + n1);
		System.out.println("Resultado N2: " + n2);
		System.out.println("Resultado N3: " + n3);
		System.out.println("Resultado N4: " + n4);
		System.out.println("Resultado N5: " + n5);
		
	}
	
}